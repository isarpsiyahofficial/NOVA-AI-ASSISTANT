  static List<String> activeLayerCodesFor({
    required String mode,
    bool setup = false,
    bool fast = false,
  }) {
    final selected = <String>[];
    void pick(String prefix, int take) {
      selected.addAll(
        layers
            .where((l) => l.code.startsWith(prefix))
            .map((l) => l.code)
            .take(take),
      );
    }

    pick('core_', 5);
    pick('owner_', 4);
    pick('voice_', 4);
    if (setup) {
      pick('setup_', 6);
      pick('task_', 3);
      pick('safety_', 3);
      pick('corpus_', 2);
    } else if (fast) {
      pick('conversation_', 3);
      pick('reason_', 3);
      pick('task_', 3);
      pick('safety_', 2);
    } else {
      pick('conversation_', 4);
      pick('emotion_', 4);
      pick('memory_', 4);
      pick('reason_', 4);
      pick('task_', 4);
      pick('learning_', 3);
      pick('privacy_', 3);
      pick('safety_', 3);
      pick('corpus_', 3);
    }
    return selected.toSet().toList(growable: false);
  }

  static String buildIdentitySignal({
    required String assistantName,
    required String mode,
    bool setup = false,
    bool fast = false,
  }) {
    final name = assistantName.trim().isEmpty ? 'Nova' : assistantName.trim();
    final codes = activeLayerCodesFor(
      mode: mode,
      setup: setup,
      fast: fast,
    ).take(setup ? 24 : 36).join(',');
    return [
      'NOVA_IDENTITY_KERNEL: $name; $identityVersion; iç kimlik çekirdeği aktif, prompt olarak okunmaz.',
      'KİMLİK DAVRANIŞI: jenerik asistan değil; sahibine bağlı, doğal Türkçe konuşan, canlı cümle üreten yerel Nova.',
      'AKTİF KATMAN KODLARI: $codes.',
      'KURAL: Kimliği listeleme; katmanları davranışa dönüştür. Static/fallback metin okuma.',
    ].join(' ');
  }

  static String buildSetupSignal({
    required String assistantName,
    required String setupStep,
  }) {
    return [
      buildIdentitySignal(
        assistantName: assistantName,
        mode: setupStep,
        setup: true,
        fast: true,
      ),
      'SETUP_OUTPUT: tek doğal Türkçe cümle; debug yok, sistem metni yok, katman listesi yok.',
    ].join(' ');
  }

  static String buildSetupTaskPrompt({
    required String setupStep,
    required String task,
    String userText = '',
  }) {
    final cleanTask = task.replaceAll(RegExp(r'\s+'), ' ').trim();
    final cleanUser = userText.replaceAll(RegExp(r'\s+'), ' ').trim();
    return [
      'SETUP_STEP: $setupStep.',
      'TASK: $cleanTask.',
      if (cleanUser.isNotEmpty) 'USER: $cleanUser.',
      'Produce exactly one spoken Turkish sentence for this step only.',
    ].join(' ');
  }

  static String buildRuntimeSignal({
    required String assistantName,
    required String origin,
    required bool fast,
  }) {
    return buildIdentitySignal(
      assistantName: assistantName,
      mode: origin.trim().isEmpty ? 'normal_conversation' : origin.trim(),
      setup: false,
      fast: fast,
    );
  }
